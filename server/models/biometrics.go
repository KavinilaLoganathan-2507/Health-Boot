package models

import (
	"go.mongodb.org/mongo-driver/bson/primitive"
)

type BiometricInput struct {
	Gender      string `json:"gender" binding:"required"` // Added for BMR & Body Fat rules
	Weight      string `json:"weight" binding:"required"`
	HeartRate   string `json:"heartRate" binding:"required"`
	SystolicBP  string `json:"systolicBP" binding:"required"`
	DiastolicBP string `json:"diastolicBP" binding:"required"`
	Water       string `json:"water" binding:"required"`
	BodyFat     string `json:"bodyFat" binding:"required"`
	Muscle      string `json:"muscle" binding:"required"`
	VisceralFat string `json:"visceralFat" binding:"required"`
	BoneMass    string `json:"boneMass" binding:"required"`
}

type BiometricAnalysisResponse struct {
	RawInput BiometricInput `json:"rawInput"`
	
	// Deterministic Analytics (Calculated in Go)
	BMI                         string `json:"bmi"`
	BMIClassification           string `json:"bmiClassification"`
	BodyFatClassification       string `json:"bodyFatClassification"`
	HeartRateClassification     string `json:"heartRateClassification"`
	BloodPressureClassification string `json:"bloodPressureClassification"`
	HydrationStatus             string `json:"hydrationStatus"`
	BMR                         string `json:"bmr"`
	DailyCalorieRequirement     string `json:"dailyCalorieRequirement"`
	IdealWeightRange            string `json:"idealWeightRange"`
	RiskScore                   string `json:"riskScore"`

	// Personalized Recommendations (Generated by Gemini AI)
	NutritionAdvice string `json:"nutritionAdvice"`
	HydrationAlerts string `json:"hydrationAlerts"`
	WellnessGoals   string `json:"wellnessGoals"`
	LifestyleTips   string `json:"lifestyleTips"`
	MealPlanForDay  string `json:"mealPlanForDay"`
}

type BiometricScanRecord struct {
	ID        primitive.ObjectID        `json:"id" bson:"_id,omitempty"`
	UserID    primitive.ObjectID        `json:"userId" bson:"userId"`
	CreatedAt primitive.DateTime        `json:"createdAt" bson:"createdAt"`
	Analysis  BiometricAnalysisResponse `json:"analysis" bson:"analysis"`
}
